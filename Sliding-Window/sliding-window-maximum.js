/* Sliding Window Maximum
Problem Statement

Given an integer array and an integer K, return an array containing the maximum value from every contiguous subarray of size K.

Example

Input:
[1, 3, -1, -3, 5, 3, 6, 7], K = 3

Output:
[3, 3, 5, 5, 6, 7]

Explanation

Each value in the output represents the maximum element found within a window of size 3 as it slides from left to right. */