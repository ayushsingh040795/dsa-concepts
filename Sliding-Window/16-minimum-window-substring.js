/* 
Minimum Window Substring
Problem Statement
    Given two strings S and T, find the smallest substring in S that contains all characters of T, including duplicates.
    If no such substring exists, return an empty string.
Example
    Input:
        S = "ADOBECODEBANC"
        T = "ABC"
    Output:
        "BANC"
Explanation
    The substring "BANC" contains A, B, and C.
    No shorter substring in S contains all required characters, so "BANC" is the minimum valid window.
*/