/* 
Shortest Subarray with Sum at Least K
Problem Statement
    Given an integer array that may contain negative numbers and an integer K, return the length of the shortest non-empty contiguous subarray whose sum is at least K.
    If no such subarray exists, return -1.
Example
    Input:
        [2, -1, 2], K = 3
    Output:
        3
Explanation
    The entire array sums to 3, which meets the requirement.
    No smaller subarray reaches a sum of K. 
*/