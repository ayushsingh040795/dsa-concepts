/* 
Count of Anagram Substrings
Problem Statement
Given a string and a pattern, count how many substrings of the string are anagrams of the pattern.
An anagram contains the same characters with the same frequencies, possibly in a different order.
Example
Input:
string = "forxxorfxdofr"
pattern = "for"
Output:
3
Explanation
The substrings "for", "orf", and "ofr" are all anagrams of the pattern "for".
There are exactly three such substrings.
*/