/* 

Longest Substring with Same Letters After Replacement
Problem Statement

Given a string and an integer K, you may replace at most K characters.
Find the length of the longest substring that can be transformed into a string with all identical characters.

Example

Input:
"aabccbb", K = 2

Output:
5

Explanation

By replacing two characters, the substring "bccbb" can be converted into "bbbbb".
No longer substring can be made uniform using only K = 2 replacements.
*/