/*
Maximum Sum of Distinct Subarrays of Size K
Problem Statement
    Given an integer array and an integer K, find the maximum possible sum of any contiguous subarray of size K such that all elements are distinct.
Example
    Input:
        [4, 2, 4, 5, 6], K = 3
    Output
        15
Explanation
    The subarray [4, 5, 6] contains distinct elements and has the maximum possible sum (15).
    Other subarrays of size 3 either have duplicates or a smaller sum. 
*/